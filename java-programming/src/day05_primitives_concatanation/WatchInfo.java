package day05_primitives_concatanation;

public class WatchInfo {
    public static void main (String [] args) {
        String brand = "Cartier";
        String color = "Silver";
        double price = 30000;
        boolean waterResistant = true;
        boolean isSmart = true;
        char gender = 'M';

        System.out.println ("The brand:\t\t\t\t\t " + brand);
        System.out.println ("The color:\t\t\t\t\t " + color);
        System.out.println ("The price:\t\t\t\t\t $" + price);
        System.out.println ("Is it water resistant?  \t " + waterResistant);
        System.out.println ("Is it Smart?   \t\t\t\t " + isSmart);
        System.out.println ("The gender:    \t\t\t\t " + gender);


    }
}


